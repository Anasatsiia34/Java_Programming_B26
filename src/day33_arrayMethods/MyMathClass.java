package day33_arrayMethods;

public class MyMathClass {
    public static void add(double numOne, double numTwo){
        System.out.println(numOne + " + " + numTwo + " = " + (numOne + numTwo));

    }
    public static void substract(double numOne, double numTwo){
        System.out.println(numOne + " - " + numTwo + " = " + (numOne - numTwo));
    }
    public static void milt (double numOne, double numTwo){
        System.out.println(numOne + " * " + numTwo + " = " + (numOne * numTwo));
    }
    public static void sub( double numOne, double numTwo){
        System.out.println(numOne + " / " + numTwo + " = " + (numOne / numTwo));
    }
}
