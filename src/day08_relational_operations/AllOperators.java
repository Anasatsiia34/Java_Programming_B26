package day08_relational_operations;

public class AllOperators {
    public static void main(String[] args) {

        System.out.println(5 + 2 * 3);

        System.out.println(5 * 2 + 4 / 2);

        System.out.println(5 + 2 > 4 - 3);

        int a = 20;
        int b = -a-- + a++ + --a * a--;
        System.out.println(b);

    }
}
